    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        ArrayList<Item> dados = data.getItens();
        
        String[] nomeColunas={"titulo", "Diretor", "Duracao", "Tenho", "Descricao" };
        String[][] valorLinhas = new String[dados.size()][5];
        
        for(int i = 0; i < dados.size(); i++) {
            valorLinhas[1][0] = dados.get(i).getTitulo();
            valorLinhas[1][1] = dados.get(i).getDiretor();
            valorLinhas[1][2] = String.valueOf(dados.get(i).getTempoDuracaoSegundos());
            valorLinhas[1][3] = String.valueOf(dados.get(i).isTenho());
            valorLinhas[1][4] = dados.get(i).getDescricao(); 
        }
        
        DefaultTableModel tabelaDados = new DefaultTableModel(valorLinhas, nomeColunas);
        
        this.tabela.setModel(tabelaDados);
        
        
    }//GEN-LAST:event_formWindowActivated

